    //prosedur simpan data 
    private void SimpanData() 
    { 
	if(txtKode.getText().equals("")||txtNama.getText().equals("")) 
	{ 
            JOptionPane.showMessageDialog(null,"Data harus diisi semua"); 
	} 
	else 
	{ 
            try 
            { 
		objStok.AksesData(); 
		objStok.KodeB = txtKode.getText(); 
		objStok.NamaB = txtNama.getText(); 
		objStok.KodeK = cboKatagori.getSelectedItem().toString().substring(0, 3); 
		String mHargaB = txtHargaB.getText(); 
		objStok.HargaBeli = Integer.parseInt(mHargaB);
                String mHargaJ = txtHargaJ.getText(); 
		objStok.HargaJual = Integer.parseInt(mHargaJ);
                String mStok = txtStok.getText(); 
		objStok.Stok = Integer.parseInt(mStok);
		objStok.Simpan(); 
				
		objStok.res.executeUpdate(objStok.sql); 
		objStok.conn.close(); 
		BersihIsian(); 
		IsiTabel(); 
		JOptionPane.showMessageDialog(null,"Data berhasil disimpan"); 
            } 
            catch (Exception e) 
            { 
		JOptionPane.showMessageDialog(null,"Proses penyimpanan gagal/koneksi gagal"); 
		System.out.println(e.getMessage()); 
            } 
	} 
    }
        
    //Prosedur menampilkan data ke jTable 
    private void IsiTabel() 
    { 
	DefaultTableModel tabelmapel = new DefaultTableModel(); 
		
	tabelmapel.addColumn("Kode Barang"); 
	tabelmapel.addColumn("Nama Barang"); 
	tabelmapel.addColumn("Kode Katagori"); 
	tabelmapel.addColumn("Katagori"); 
	tabelmapel.addColumn("Harga Beli");
        tabelmapel.addColumn("Harga Jual");
        tabelmapel.addColumn("Stok");
        tabelmapel.addColumn("Total H Beli");
        tabelmapel.addColumn("Total H Jual");
        tabelmapel.addColumn("Keterangan");
	try 
	{ 
            objStok.AksesData(); 
            objStok.TampilData(); 
			
            Statement stat1 = objStok.conn.createStatement(); 
            ResultSet res1=stat1.executeQuery(objStok.sql); 
            while (res1.next()) 
            { 
		tabelmapel.addRow(new Object[]{res1.getString(1),res1.getString(2),res1.getString(3),res1.getString(4),res1.getString(5),res1.getString(6),
                    res1.getString(7),res1.getString(8),res1.getString(9),res1.getString(10)}); 
            } 
            jTabel.setModel(tabelmapel); 
            AturKolom(); //atur kolom jTable 
	} 
	catch (Exception e) 
	{ 
	} 
    }
	
    //prosedur mengisi combobox dengan data 
    public void IsiComboKatagori() 
    { 
        try 
	{ 
            cboKatagori.removeAllItems(); 
            objStok.AksesData(); 
            String sql1 = "select * from tblkatagori"; 
            Statement stat1 = objStok.conn.createStatement(); 
            ResultSet res1=stat1.executeQuery(sql1); 
            cboKatagori.addItem("Pilih Katagori"); 
            while(res1.next()) 
            { 
		cboKatagori.addItem(res1.getString("KodeK") + " : " + res1.getString("Katagori")); 
            } 
	} 
	catch(Exception e) 
	{
	} 
    }
    
