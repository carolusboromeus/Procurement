    private void jTabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelMouseClicked
        // TODO add your handling code here:
        int baris = jTabel.getSelectedRow(); 
	String kolom1 = jTabel.getValueAt(baris, 0).toString(); 
	String kolom2 = jTabel.getValueAt(baris, 1).toString(); 
	String kolom3 = jTabel.getValueAt(baris, 2).toString() +" : "+ jTabel.getValueAt(baris, 3).toString(); 
	String kolom5 = jTabel.getValueAt(baris, 4).toString();
        String kolom6 = jTabel.getValueAt(baris, 5).toString(); 
        String kolom7 = jTabel.getValueAt(baris, 6).toString(); 
		
	txtKode.setText(kolom1); 
	txtNama.setText(kolom2); 
	cboKatagori.removeAllItems(); 
	cboKatagori.addItem(kolom3);
	txtHargaB.setText(kolom5);
        txtHargaJ.setText(kolom6);
        txtStok.setText(kolom7);
    }//GEN-LAST:event_jTabelMouseClicked

